// Strings o cadenas de texto

const tweet = "Aprendiendo JavaScript con el Curso de Desarrollo Web Completo";
const producto2 = "Monitor HD";

// Métodos para los Strings

// length es para saber la longitud del String
console.log(tweet.length);

// indexOF (Nos permite saber la posición de un elemento (palabra o letra) en un texto)
console.log(tweet.indexOf("JavaScript"));
console.log(producto2.indexOf("Monitor"));

// includes (Retorna "true" o "false")
console.log(tweet.includes('Curso'));
console.log(producto2.includes('Curso'));
