function sumar(num1, num2) {
  // Lo que va dentro del paréntesis, se le conoce como parámetro
  console.log(num1 + num2);
}
sumar(10, 10); // Y lo que va dentro de estos paréntesis se les conoce como argumentos
sumar(50, 10);
sumar(1, 1);
sumar(3, 5);
sumar(1, 12);

const sumar2 = function (n1, n2) {
  console.log(n1 + n2);
};
sumar2(5, 10);
